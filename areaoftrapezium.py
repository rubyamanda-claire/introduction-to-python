#Area=1\2*(a+b)*h

#Input Values
a = float(input("Enter the length of first parallel side (a): "))
b = float(input("Enter the length of second parallel side (b): "))
h = float(input("Enter the height (h): "))

# Calculate area
area = 0.5 * (a + b) * h

# Display result
print("The Area of the Trapezium is:", area)