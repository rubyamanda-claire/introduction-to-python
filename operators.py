#Arithmetic Operators

x = 10
y = 5
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) #Modulus - Returns the remainder

#Comparison Operators - Used to compare values and give a result

print(x < y)
print(x > y)
print(x >= y)
print(x <= y)
print(x == y) #Equal to
print(x != y) #Not equal to

#Assignment Operators -
a = 56
print(a)

#Operator Precedence - Order in which operations get executed
a += 2
print(a)
a -= 2
print(a)
a *= 2
print(a)
a /= 2
print(a)

#Logical Operators - and, or, not (Have more than one condition)
print(100>200 and 100==200) # Only returns true if both conditions are true
print(100>200 or 200==100) # When any of the conditions is true
print(not(100>200 or 200==100)) # Gives the opposite of result

